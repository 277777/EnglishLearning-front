package com.ELfront.po.Word;

public class Parts {

	private String part;
	private String means;
	public Parts() {
		super();
	}
	public Parts(String part, String means) {
		super();
		this.part = part;
		this.means = means;
	}
	public String getPart() {
		return part;
	}
	public void setPart(String part) {
		this.part = part;
	}
	public String getMeans() {
		return means;
	}
	public void setMeans(String means) {
		this.means = means;
	}
	
	
}
